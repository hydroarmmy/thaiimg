sub build_seo_section {
  my $p = shift;
  my $html = qq|

  <!-- SEO content section -->
  <section class="tool-seo">
    <h2>$p->{seo_h2}</h2>
    <p>$p->{seo_p}</p>

    <h3>วิธีใช้งาน</h3>
    <ol>
|;
  for my $step (@{ $p->{how_to} }) {
    $html .= "      <li>$step</li>\n";
  }
  $html .= "    </ol>\n\n    <h3>คุณสมบัติเด่น</h3>\n    <ul>\n";
  for my $feat (@{ $p->{features} }) {
    $html .= "      <li>$feat</li>\n";
  }
  $html .= "    </ul>\n\n    <h3>คำถามที่พบบ่อย</h3>\n";
  for my $faq (@{ $p->{faqs} }) {
    $html .= qq|    <details class="tool-faq">
      <summary>$faq->{q}</summary>
      <p>$faq->{a}</p>
    </details>
|;
  }
  $html .= "  </section>\n";
  return $html;
}

for my $file (sort keys %pages) {
  my $info = $pages{$file};

  open my $fh, '<:encoding(UTF-8)', $file or do { warn "Cannot read $file: $!\n"; next };
  local $/;
  my $html = <$fh>;
  close $fh;

  # Remove existing tool-seo section if present (re-run safe)
  $html =~ s|\s*<!-- SEO content section -->\s*<section class="tool-seo">.*?</section>||s;

  # Replace H1 with new keyword-rich version
  $html =~ s|<h1>[^<]+</h1>|<h1>$info->{h1}</h1>|;

  # Replace lead paragraph (the <p> right under H1 inside .page-header)
  # Match: <p>...</p> immediately following the H1
  $html =~ s|(<h1>\Q$info->{h1}\E</h1>\s*)<p>.*?</p>|$1<p>$info->{lead}</p>|s;

  # Insert SEO section right before </main>
  my $seo = build_seo_section($info);
  $html =~ s|(\s*</main>)|$seo$1|;

  open my $out, '>:encoding(UTF-8)', $file or die "Cannot write $file: $!\n";
  print $out $html;
  close $out;

  print "OK: $file\n";
}
